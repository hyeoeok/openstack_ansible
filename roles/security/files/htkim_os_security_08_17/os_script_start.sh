sc_list=$(ls -v | grep ^[0-9] | egrep -v txt$ )

for i in $sc_list
do
        echo "================================================ start $i ================================================"
        type_txt=$(cat $i | grep 'un_change_list.txt')
        if [[ "$type_txt" == "" ]];
        then
                echo "================================================ start $i ================================================" >> change_list.txt
                ./$i
                echo -e "================================================ end $i ================================================\n" >> change_list.txt
        else

                echo "================================================ start $i ================================================" >> un_change_list.txt
                ./$i
                echo -e "================================================ end $i ================================================\n" >> un_change_list.txt
        fi

        echo -e "================================================ end $i ================================================\n"
done
